package T2;
public class AvgOfThree {
    
    public static void main(String[] args){
        
        int n1 = 11;
        int n2 = 20;
        int n3 = 30;
        
        int total = n1+n2+n3;
        
        float avg = (float)total/3;       
        
        System.out.println(" Total : " + total );
        System.out.println(" Average : " + avg );
        
    }
    
}
